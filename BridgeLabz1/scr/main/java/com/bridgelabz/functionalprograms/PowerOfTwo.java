package com.bridgelabz.functionalprograms;


import com.bridgelabz.utility.UtilityScanner;

/** Power of 2:This program takes a command­line argument N and prints a table of the
 *  powers of 2 that are less than or equal to 2^N. 
 *  @author LOKESH NAIK
 * 
 * */


public class PowerOfTwo
{
  public static void main(String[] args)
  {
	  System.out.println("Enter the number upto which you have print power if 2:");
	    int number=UtilityScanner.integerInput(); //Taking integer input from the user to calculate till 2^N value
	    int i=0;
		   while(i!=number+1)
		   {
			   System.out.println((int)(Math.pow(2, i)));
			   i++;
		   }
  }
}
